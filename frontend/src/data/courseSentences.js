// Helper lookups
export function byKey(key) {
  if (!key) return null;
  return categories.find((c) => c.key === key) || null;
}

/**
 * getSentence(categoryKey, idxOrId)
 * - idxOrId may be a number (0-based index) or a string id.
 */
export function getSentence(categoryKey, idxOrId) {
  const cat = byKey(categoryKey);
  if (!cat) return null;

  if (typeof idxOrId === "number") {
    return cat.sentences[idxOrId] || null;
  }
  if (typeof idxOrId === "string") {
    return cat.sentences.find((s) => s.id === idxOrId) || null;
  }
  return null;
}

